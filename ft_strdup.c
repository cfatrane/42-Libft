#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t		i;
	size_t	len;

	i = 0;
	len = ft_strlen(s1);
	s2 = (char*)malloc(sizeof(*s2) * (len + 1));
	if (!s2)
		return (NULL);
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
