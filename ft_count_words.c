#include "libft.h"

int	count_words(char const *s)
{
	int count;
	int i;

	i = 0;
	count = 0;
	while (s[i] != '\0')
	{
		if (s[i] != ' ' || s[i] != '\t' || s[i] != '\n')
			count++;
 		i++;
	}
	return (count);
}
